// src/components/ParticleBackground.js
import React, { useCallback } from 'react'; // <-- Import useCallback
import Particles from "react-tsparticles";
// import { loadFull } from "tsparticles"; // <-- REMOVE THIS LINE
import { loadSlim } from "tsparticles-slim"; // <-- ADD THIS LINE

const ParticleBackground = () => {
  // Use useCallback to memoize the function, ensuring it's not recreated on every render
  const particlesInit = useCallback(async (engine) => {
    // This loads the slim version of the engine, which is all we need
    await loadSlim(engine);
  }, []);

  return (
    <Particles
      id="tsparticles"
      init={particlesInit}
      options={{
        background: {
          color: {
            value: "#121212",
          },
        },
        fpsLimit: 60,
        interactivity: {
          events: {
            onHover: {
              enable: true,
              mode: "repulse",
            },
            resize: true,
          },
          modes: {
            repulse: {
              distance: 100,
              duration: 0.4,
            },
          },
        },
        particles: {
          color: {
            value: "#ffffff",
          },
          links: {
            color: "#ffffff",
            distance: 150,
            enable: true,
            opacity: 0.2,
            width: 1,
          },
          collisions: {
            enable: true,
          },
          move: {
            direction: "none",
            enable: true,
            outModes: {
              default: "bounce",
            },
            random: false,
            speed: 1,
            straight: false,
          },
          number: {
            density: {
              enable: true,
              area: 800,
            },
            value: 80,
          },
          opacity: {
            value: 0.2,
          },
          shape: {
            type: "circle",
          },
          size: {
            value: { min: 1, max: 5 },
          },
        },
        detectRetina: true,
      }}
    />
  );
};

export default ParticleBackground;